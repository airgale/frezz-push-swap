#include "push_swap.h"

int	str_cmp(char *s1, char *s2)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (s1[i] == '+')
	{
		if (s2[j] != '+')
			i++;
	}
	if (s1[i] != '+')
	{
		if (s2[j] == '+')
			j++;
	}
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[j])
	{
		i++;
		j++;
	}
	return (s1[i] - s2[j]);
}
